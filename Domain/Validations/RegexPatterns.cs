using System.Text.RegularExpressions;

namespace Domain.Validations;

/// <summary>
/// Паттерны регулярных выражений
/// </summary>
public static class RegexPatterns
{
    /// <summary>
    /// Номер телефона
    /// </summary>
    public static readonly Regex PhonePattern = new(@"^\+373\d{8}$");

    /// <summary>
    /// Никнейм телеграма
    /// </summary>
    public static readonly Regex TelegramPattern = new("^[A-Za-z0-9_]{5,32}$");

    /// <summary>
    /// ФИО
    /// </summary>
    public static readonly Regex LfmPattern = new(@"^[а-яА-ЯёЁa-zA-Z]{1,50}\\s[а-яА-ЯёЁa-zA-Z]{1,50}\\s?[а-яА-ЯёЁa-zA-Z]{0,50}$");
}

